"""
Dados dos números, mostrar la suma, resta, división y multiplicación de ambos.

"""

numb1 = int(input("Escriba el numero 1: "))
numb2 = int(input("Escriba el numero 2: "))
sum = numb1 + numb2
minus = numb1 - numb2
division = numb1 / numb2
multiplication = numb1 * numb2

print("La suma entre los dos numeros es: ", sum)
print("La resta entre los dos numeros es: ", minus)
print("La division entre los dos numeros es: ", division)
print("La multiplicacion entre los dos numeros es: ", multiplication)

