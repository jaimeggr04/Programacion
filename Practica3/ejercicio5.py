"""
Escribir un programa que convierta un valor dado en grados Fahrenheit a grados Celsius.

"""
# Realizar la conversión de grados Fahrenheit a grados Celsius utilizando la fórmula
Fahrenheit = float(input("Escribra los grados Fahrenheit: "))
Conversion = (Fahrenheit - 32) / 1.8

print("El resutado de su conversion es: ", Conversion, "°C")
