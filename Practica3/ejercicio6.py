"""
Calcular la media de tres números pedidos por teclado
"""

numb1 = int(input("Escriba el numero 1: "))
numb2 = int(input("Escriba el numero 2: "))
numb3 = int(input("Escriba el numero 3: "))
sum = numb1 + numb2 + numb3
# Calcular la media (promedio) de los tres números
average = sum / 3

print("La media de los valores es: ", average)
